% --- Executes when user attempts to close error_dialog.
function error_dialog_CloseRequestFcn(hObject, eventdata, handles)
% hObject    handle to error_dialog (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

if isequal(get(hObject, 'waitstatus'), 'waiting')
    % The GUI is still in UIWAIT, us UIRESUME
    uiresume(hObject);
else
    % The GUI is no longer waiting, just close it
    delete(hObject);
end


% --- Executes on key press over error_dialog with no controls selected.
function error_dialog_KeyPressFcn(hObject, eventdata, handles)
% hObject    handle to error_dialog (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Check for "enter" or "escape"
if isequal(get(hObject,'CurrentKey'),'escape')
    % User said no by hitting escape
    handles.output = 'No';
    
    % Update handles structure
    guidata(hObject, handles);
    
    uiresume(handles.error_dialog);
end    
    
if isequal(get(hObject,'CurrentKey'),'return')
    uiresume(handles.error_dialog);
end    



